// –ù–æ—Ä–º–∞–ª—ñ–∑–∞—Ü—ñ—è —Ç–µ–∫—Å—Ç—É: –ø—Ä–∏–±–∏—Ä–∞—î–º–æ –¥—ñ–∞–∫—Ä–∏—Ç–∏—á–Ω—ñ –∑–Ω–∞–∫–∏, –ø—É–Ω–∫—Ç—É–∞—Ü—ñ—é, –∑–≤–æ–¥–∏–º–æ –¥–æ –Ω–∏–∂–Ω—å–æ–≥–æ —Ä–µ–≥—ñ—Å—Ç—Ä—É
function normalizeText(str) {
    if (!str) return '';
    return str.toString()
        .normalize('NFKD')                         // —Ä–æ–∑–¥—ñ–ª—è—î –¥—ñ–∞–∫—Ä–∏—Ç–∏—á–Ω—ñ –∑–Ω–∞–∫–∏
        .replace(/[\u0300-\u036f]/g, '')           // –≤–∏–¥–∞–ª—è—î–º–æ –¥—ñ–∞–∫—Ä–∏—Ç–∏—á–Ω—ñ –∑–Ω–∞–∫–∏
        .toLowerCase()
        .replace(/[^\p{L}\p{N}\s]/gu, ' ')         // –∑–∞–ª–∏—à–∞—î–º–æ –ª–∏—à–µ –±—É–∫–≤–∏, —Ü–∏—Ñ—Ä–∏ —Ç–∞ –ø—Ä–æ–±—ñ–ª–∏ (Unicode-safe)
        .replace(/\s+/g, ' ')
        .trim();
}

// –†–æ–∑—à–∏—Ä–µ–Ω–∏–π –ø–æ—à—É–∫ –ø–æ FAQ ‚Äî –∑–Ω–∞—Ö–æ–¥–∏—Ç—å –∫–ª—é—á–æ–≤—ñ —Å–ª–æ–≤–∞
function searchFAQ(question) {
    const normQuestion = normalizeText(question);
    if (!normQuestion) return { found: false };

    // –ó–±–µ—Ä—ñ–≥–∞—î–º–æ –Ω–∞–π–∫—Ä–∞—â–∏–π —á–∞—Å—Ç–∫–æ–≤–∏–π –º–∞—Ç—á –Ω–∞ –≤–∏–ø–∞–¥–æ–∫, —è–∫—â–æ –ø–æ–≤–Ω–∏—Ö –Ω–µ–º–∞—î
    let bestPartial = { found: false, answer: null, confidence: 0, keyword: null };

    for (const category of Object.values(FAQ_DATABASE)) {
        for (const item of Object.values(category)) {
            for (const keyword of item.keywords) {
                const normKeyword = normalizeText(keyword);
                if (!normKeyword) continue;

                // 1) –ü—Ä—è–º–∏–π –ø—ñ–¥—Ä—è–¥–∫–æ–≤–∏–π –º–∞—Ç—á (—Ñ—Ä–∞–∑–∞ —Ü—ñ–ª–∫–æ–º –º—ñ—Å—Ç–∏—Ç—å—Å—è –≤ –ø–∏—Ç–∞–Ω–Ω—ñ)
                if (normQuestion.includes(normKeyword)) {
                    const confidence = calculateConfidence(normQuestion, item.keywords);
                    return { found: true, answer: item.answer, confidence, matchedKeyword: keyword };
                }

                // 2) –Ø–∫—â–æ –≤—Å—ñ —Ç–æ–∫–µ–Ω–∏ –∫–ª—é—á–æ–≤–æ–≥–æ —Å–ª–æ–≤–∞ –ø—Ä–∏—Å—É—Ç–Ω—ñ (–Ω–∞–≤—ñ—Ç—å –≤ —ñ–Ω—à–æ–º—É –ø–æ—Ä—è–¥–∫—É)
                const kTokens = normKeyword.split(' ').filter(Boolean);
                if (kTokens.length > 0) {
                    const matchedTokens = kTokens.filter(t => normQuestion.includes(t)).length;
                    if (matchedTokens === kTokens.length) {
                        const confidence = calculateConfidence(normQuestion, item.keywords);
                        return { found: true, answer: item.answer, confidence, matchedKeyword: keyword };
                    }

                    // 3) –ß–∞—Å—Ç–∫–æ–≤–∏–π –º–∞—Ç—á ‚Äî –∑–±–∏—Ä–∞—î–º–æ –Ω–∞–π–∫—Ä–∞—â–∏–π –¥–ª—è –º–æ–∂–ª–∏–≤–æ–≥–æ –ø–æ–≤–µ—Ä–Ω–µ–Ω–Ω—è
                    const partialScore = Math.round((matchedTokens / kTokens.length) * 100);
                    if (partialScore > bestPartial.confidence && partialScore >= 40) { // –º—ñ–Ω—ñ–º—É–º 40% –¥–ª—è —á–∞—Å—Ç–∫–æ–≤–æ–≥–æ
                        bestPartial = { found: true, answer: item.answer, confidence: partialScore, keyword };
                    }
                }
            }
        }
    }

    // –Ø–∫—â–æ –∑–Ω–∞–π—à–ª–∏ –ø—Ä–∏–π–Ω—è—Ç–Ω–∏–π —á–∞—Å—Ç–∫–æ–≤–∏–π –º–∞—Ç—á ‚Äî –ø–æ–≤–µ—Ä—Ç–∞—î–º–æ –π–æ–≥–æ
    if (bestPartial.found) {
        return { found: true, answer: bestPartial.answer, confidence: bestPartial.confidence, matchedKeyword: bestPartial.keyword };
    }

    return { found: false };
}

// –†–æ–∑—Ä–∞—Ö—É–Ω–æ–∫ "–≤–ø–µ–≤–Ω–µ–Ω–æ—Å—Ç—ñ" ‚Äî –æ—Ü—ñ–Ω—é—î–º–æ –ø–æ –∫—Ä–∞—â–æ–º—É –∫–ª—é—á–æ–≤–æ–º—É —Å–ª–æ–≤—É
function calculateConfidence(question, keywords) {
    const nq = normalizeText(question);
    let best = 0;

    for (const kw of keywords) {
        const nk = normalizeText(kw);
        if (!nk) continue;

        if (nq.includes(nk)) {
            best = Math.max(best, 100);
            continue;
        }

        const tokens = nk.split(' ').filter(Boolean);
        if (tokens.length === 0) continue;
        const matches = tokens.filter(t => nq.includes(t)).length;
        const score = Math.round((matches / tokens.length) * 100);
        best = Math.max(best, score);
    }

    return best;
}

// –ì–µ–Ω–µ—Ä–∞—Ü—ñ—è —à–≤–∏–¥–∫–∏—Ö –≤—ñ–¥–ø–æ–≤—ñ–¥–µ–π
function getRandomFAQs(count = 6) {
    const allItems = [];

    for (const category of Object.values(FAQ_DATABASE)) {
        for (const [key, item] of Object.entries(category)) {
            allItems.push({
                id: key,
                // –≤—ñ–¥–æ–±—Ä–∞–∂—É–≤–∞–Ω–∏–π —Ç–µ–∫—Å—Ç (–≥–∞—Ä–Ω–æ –≤–∏–≥–ª—è–¥–∞—î –Ω–∞ –∫–Ω–æ–ø—Ü—ñ)
                text: capitalizeFirst(item.keywords[0] || key) + ' ?',
                // —Ä–µ–∞–ª—å–Ω–µ –∫–ª—é—á–æ–≤–µ —Å–ª–æ–≤–æ, —â–æ –±—É–¥–µ –≤—ñ–¥–ø—Ä–∞–≤–ª–µ–Ω–æ –Ω–∞ –±–µ–∫–µ–Ω–¥ –¥–ª—è —Ç–æ—á–Ω–æ–≥–æ –ø–æ—à—É–∫—É
                question: item.keywords[0] || key,
                answer: item.answer
            });
        }
    }

    return allItems
        .sort(() => 0.5 - Math.random())
        .slice(0, count)
        .map(item => ({
            id: item.id,
            text: item.text,
            question: item.question
        }));
}

function capitalizeFirst(str) {
    return str.charAt(0).toUpperCase() + str.slice(1);
}

